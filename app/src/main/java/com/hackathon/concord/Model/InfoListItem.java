package com.hackathon.concord.Model;

public class InfoListItem {
    private String regNumber;
    private String petName;
    private String petDate;
    private String petWorkDate;
    private String petGender;
    private String petBreed;

    public String getRegNumber() {
        return regNumber;
    }

    public void setRegNumber(String regNumber) {
        this.regNumber = regNumber;
    }

    public String getPetName() {
        return petName;
    }

    public void setPetName(String petName) {
        this.petName = petName;
    }

    public String getPetDate() {
        return petDate;
    }

    public void setPetDate(String petDate) {
        this.petDate = petDate;
    }

    public String getPetWorkDate() {
        return petWorkDate;
    }

    public void setPetWorkDate(String petWorkDate) {
        this.petWorkDate = petWorkDate;
    }

    public String getPetGender() {
        return petGender;
    }

    public void setPetGender(String petGender) {
        this.petGender = petGender;
    }

    public String getPetBreed() {
        return petBreed;
    }

    public void setPetBreed(String petBreed) {
        this.petBreed = petBreed;
    }
}
